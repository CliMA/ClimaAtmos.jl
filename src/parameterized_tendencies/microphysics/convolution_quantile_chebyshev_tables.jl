@inline function chebyshev_evaluate(coeffs::SA.SVector{N, T}, τ::FT) where {N, T, FT}
    # Standard 1D Clenshaw recursion
    d = zero(FT)
    dd = zero(FT)
    τ2 = 2 * τ
    @inbounds for j in N:-1:2
        tmp = d
        d = τ2 * d - dd + coeffs[j]
        dd = tmp
    end
    return τ * d - dd + coeffs[1]
end

@inline function chebyshev_evaluate_2d(coeffs::SA.SMatrix{N, M, T}, τ1::FT, τ2::FT) where {N, M, T, FT}
    # Bivariate Clenshaw: evaluate columns to get 1D coeffs, then evaluate 1D
    # (Memory layout: SMatrix is column-major)
    col_coeffs = SA.SVector{M, FT}(
        ntuple(j -> chebyshev_evaluate(SA.SVector{N, T}(coeffs[:, j]), τ1), Val(M))
    )
    return chebyshev_evaluate(col_coeffs, τ2)
end
